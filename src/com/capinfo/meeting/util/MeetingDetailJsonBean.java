package com.capinfo.meeting.util;

import java.util.List;

import com.capinfo.meeting.bean.Meeting;
import com.capinfo.meeting.bean.Meeting_Doc;
import com.capinfo.meeting.bean.Meeting_Leader;
import com.capinfo.meeting.bean.Meeting_Person;
import com.capinfo.meeting.bean.Meeting_Time;

public class MeetingDetailJsonBean {
	private Meeting meeting;
	private List<Meeting_Doc> docs;
	private List<Meeting_Leader> leaders;
	private List<Meeting_Person> persons;
	private List<Meeting_Time> times;

	public MeetingDetailJsonBean(Meeting meeting, List<Meeting_Doc> docs,
			List<Meeting_Leader> leaders, List<Meeting_Person> persons,
			List<Meeting_Time> times) {
		this.meeting = meeting;
		this.docs = docs;
		this.leaders = leaders;
		this.persons = persons;
		this.times = times;
	}

	public MeetingDetailJsonBean() {
	}

	public Meeting getMeeting() {
		return meeting;
	}

	public void setMeeting(Meeting meeting) {
		this.meeting = meeting;
	}

	public List<Meeting_Doc> getDocs() {
		return docs;
	}

	public void setDocs(List<Meeting_Doc> docs) {
		this.docs = docs;
	}

	public List<Meeting_Leader> getLeaders() {
		return leaders;
	}

	public void setLeaders(List<Meeting_Leader> leaders) {
		this.leaders = leaders;
	}

	public List<Meeting_Person> getPersons() {
		return persons;
	}

	public void setPersons(List<Meeting_Person> persons) {
		this.persons = persons;
	}

	public List<Meeting_Time> getTimes() {
		return times;
	}

	public void setTimes(List<Meeting_Time> times) {
		this.times = times;
	}

}
